#include "3-calc.h"
/**
 * op_add - a function that adds two integers
 * op_mul - finds the product of two integers
 * op_div - finds a / b
 * op_mod - finds a % b
 * @a: first int
 * @b: second int
 * Return: an int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function finds the difference
 * between two integers
 * @a: first int
 * @b: second int
 * Return: an int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - finds the product of two integers
 * @a: first int
 * @b: second int
 * Return: an int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - finds a / b
 * @a: first int
 * @b: second int
 * Return: an int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds a % b
 * @a: first int
 * @b: second int
 * Return: an int
 */
int op_mod(int a, int b)
{
	return (a % b);
}

